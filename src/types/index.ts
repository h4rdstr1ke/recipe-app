/**
 * Основная информация о пользователе системы.
 */
export interface User {
    /** Уникальный идентификатор пользователя */
    id: string;
    /** Электронная почта */
    email: string;
    /** Уникальное имя пользователя (логин), отображаемое в URL и комментариях */
    nickname: string;
    /** Настоящее или экранное имя для отображения в профиле */
    name: string;
    /** Ссылка на изображение профиля. Если null, используется стандартный аватар */
    avatarUrl?: string | null;
    /** Краткая информация о себе (биография) */
    bio?: string;
}

/**
 * Личные настройки, предпочтения и связи пользователя.
 * Хранятся глобально и используются для фильтрации контента.
 */
export interface UserSettings {
    /** Список ингредиентов, на которые у пользователя аллергия */
    allergens: string[];
    /** Список продуктов, которые пользователь не хочет видеть в рецептах */
    unwanted: string[];
    /** Массив ID авторов, на которых подписан текущий пользователь */
    subscriptions: string[];
    /** Массив ID постов, которым пользователь поставил лайк */
    likedPosts: string[];
    /** Массив ID постов, сохраненных в закладки */
    favoritePosts: string[];
}

/**
 * Данные автора для отображения в рейтинге (Топ-авторов).
 */
export interface TopAuthor {
    /** ID автора, соответствующий User.id */
    id: string;
    /** Публичный никнейм */
    username: string;
    /** Ссылка на аватар */
    avatarUrl: string | null;
    /** Общее количество опубликованных рецептов */
    postsCount: number;
    /** Количество подписчиков */
    subscribersCount: number;
    /** Внутренний скоринг рейтинга для сортировки в топе */
    ratingScore: number;
}

/**
 * Полная структура рецепта (поста).
 */
export interface Post {
    /** Уникальный ID публикации */
    id: string;
    /** ID создателя поста */
    authorId: string;
    /** Никнейм автора на момент публикации */
    username: string;
    /** Имя автора для отображения в ленте */
    firstName: string;
    /** Аватар автора */
    authorAvatar: string | null;
    /** Главное изображение готового блюда */
    image: string;
    /** Заголовок рецепта */
    title: string;
    /** Краткое описание или история блюда */
    description: string;
    /** Дата публикации в строковом формате */
    date: string;
    /** Данные о пользовательском рейтинге поста */
    rating?: {
        /** Средний балл (например, от 1 до 5) */
        rating: number;
        /** Количество проголосовавших пользователей */
        quantity: number;
    };
    /** Время приготовления */
    timeCooking: string;
    /** Суммарное количество лайков */
    likesCount: number;
    /** Сколько раз рецепт добавили в избранное */
    favoritesCount: number;
    /** Количество комментариев */
    commentsCount: number;
    /** Пищевая ценность на одну порцию */
    nutrition?: {
        calories: number;
        protein: number;
        fat: number;
        carbs: number;
    };
    /** Количество порций */
    portions?: number;
    /** Список необходимых ингредиентов */
    products?: {
        /** Название ингредиента */
        name: string;
        /** Числовое значение количества */
        quantity: number;
        /** Единица измерения (выбор из фиксированного списка) */
        unit: 'шт' | 'г' | 'мл' | 'ст.л' | 'ч.л' | 'ст';
    }[];
    /** Пошаговое руководство по приготовлению */
    steps?: {
        /** Порядковый номер шага */
        stepNumber: number;
        /** Текстовая инструкция к шагу */
        description: string;
        /** Опциональное фото процесса для этого шага */
        image?: string;
    }[];
}

/**
 * Ответ на комментарий.
 */
export interface Reply {
    /** Уникальный ID ответа */
    id: string;
    /** Автор ответа (его никнейм) */
    author: string;
    /** Текст сообщения */
    text: string;
    /** Счетчик лайков под ответом */
    likesCount: number;
    /** Лайкнул ли этот ответ текущий авторизованный пользователь */
    isLiked: boolean;
    /** Дата и время создания в читаемом виде */
    createdAt: string;
}

/**
 * Основной комментарий к публикации.
 */
export interface Comment {
    /** Уникальный ID комментария */
    id: string;
    /** К какому посту относится комментарий */
    postId: string;
    /** Никнейм автора комментария */
    author: string;
    /** Основной текст */
    text: string;
    /** Опциональное прикрепленное изображение */
    imageUrl?: string;
    /** Счетчик лайков */
    likesCount: number;
    /** Статус лайка для текущего пользователя */
    isLiked: boolean;
    /** Время публикации */
    createdAt: string;
    /** Массив вложенных ответов (реализация дерева комментариев) */
    replies: Reply[];
}

/**
 * Расширенная модель профиля пользователя, включающая статистику.
 */
export interface UserProfile extends User {
    /** Количество людей, подписанных на этого пользователя */
    subscribersCount: number;
    /** Количество людей, на которых подписан этот пользователь */
    subscriptionsCount: number;
}